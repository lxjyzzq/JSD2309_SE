package Demo;

public class BinaryDemo {
    public static void main(String[] args) {
        int n = 45;
        System.out.println(Integer.toBinaryString(n));  //以二进制输出
        System.out.println(n);  //以十进制输出

        n++;    //将101101增加1

        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);

        //第五遍
        n = 45;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);
        n++;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);

        //第四遍
        n = 45;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);
        n++;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);

        //第三遍
        n = 45;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);
        n++;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);

        //第二遍
        n = 45;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);
        n++;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);

        //第一遍
        n = 45;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);
        n++;
        System.out.println(Integer.toBinaryString(n));
        System.out.println(n);


    }
}

